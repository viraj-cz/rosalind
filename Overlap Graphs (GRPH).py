def fasta_parser(fasta):
    results = []
    strings = fasta.strip().split('>')

    for i in strings:
        if len(i) != 0:
            parts = i.split() #this splits the string into parts based on white characters
            k = parts[0] #as we know that the first white char we encountered was after the id and before the seq, right in middle
            v = ''.join(parts[1:]) #this joins all the seq
            results.append([k, v])

    return results

