def GC_counter(dna):
    DNA = dna
    #finding start locations
    start_locations = []
    for i in range(len(DNA)):
        if (DNA[i] == ">"):
            start_locations.append(i)
            
    DNA_strings = []
    #we know that we are suing FASTA format and labelling is 13 letters long
    for i in range(len(start_locations)-1):
        DNA_strings.append(DNA[start_locations[i]+14:start_locations[i+1]])
            
            
    #appending the final one:
    DNA_strings.append(DNA[start_locations[len(start_locations)-1]+14::])
    
    #removing "\n" from DNA DNA_strings
    for i in range(len(DNA_strings)):
        DNA_strings[i] = DNA_strings[i].replace("\n", "")
        
    #finding GC %
    GCP = []
    
    
    for i in range(len(DNA_strings)):
        GC_count = 0
        total = (len(DNA_strings[i]))
        for nucleotides in DNA_strings[i]:
            if (nucleotides == "G" or nucleotides == "C"):
                GC_count += 1
        GCP.append(GC_count/total*100)
    
    highest_GC = max(GCP)
    index_of_highest = GCP.index(max(GCP))
    name_of_highest_GC = []
    name_of_highest_GC.append(DNA[start_locations[index_of_highest]+1:start_locations[index_of_highest]+ 14])
    return(name_of_highest_GC, highest_GC)
    
    
